/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
	//投食时间控制
	if(ts_flag == 0)
	{
		time_ts_ms++;
		if(time_ts_ms > 1000)
		{
			time_ts_ms = 0;
			time_ts_s--;
			if(time_ts_s == 0)
			{
				ts_flag = 1;
			}
		}
	}
	//过滤时间控制
	if(gl_flag == 0)
	{
		time_gl_ms++;
		if(time_gl_ms > 1000)
		{
			time_gl_ms = 0;
			time_gl_s--;
			if(time_gl_s == 0)
			{
				gl_flag = 1;
			}
		}
	}
  /* USER CODE END SysTick_IRQn 1 */
}

